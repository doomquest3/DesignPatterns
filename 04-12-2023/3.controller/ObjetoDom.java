public class ObjetoDom {
    private String nome;
    private String endereco;

    
    public ObjetoDom(String nome, String endereco){
        this.nome = nome;
        this.endereco = endereco;
        
    }


    public String getNome() {
        return nome;
    
    }
    public String getEndereco() {
        return endereco;
    
    }

}
